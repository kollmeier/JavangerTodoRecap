package ckollmeier.de.javangertodorecap.enums;

/**
 * Enum, das die möglichen Aktionen repräsentiert, die an einem Element durchgeführt werden können.
 */
public enum Action {
    /**
     * Repräsentiert die Aktion zum Erstellen eines neuen Elements.
     */
    CREATE,
    /**
     * Repräsentiert die Aktion zum Aktualisieren eines bestehenden Elements.
     */
    UPDATE,
    /**
     * Repräsentiert die Aktion zum Löschen eines bestehenden Elements.
     */
    DELETE
}
